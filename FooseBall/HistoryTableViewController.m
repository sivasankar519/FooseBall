#pragma mark - Table view

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return [[self.fetchedResultsController sections] count];
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    id <NSFetchedResultsSectionInfo> sectionInfo = [self.fetchedResultsController sections][section];
    return [sectionInfo numberOfObjects];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    HistoryTableViewCell *cell = (HistoryTableViewCell*)[tableView dequeueReusableCellWithIdentifier:@"HistoryCell" forIndexPath:indexPath];
    
    if(cell == nil){
        cell = [[HistoryTableViewCell alloc]initWithStyle:UITableViewCellStyleDefault reuseIdentifier:@"HistoryCell"];
    }
    [self configureCell:cell atIndexPath:indexPath];
    return cell;
}

- (void)configureCell:(UITableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath {
    
    Fooseball *object = [self.fetchedResultsController objectAtIndexPath:indexPath];
    HistoryTableViewCell *customCell = (HistoryTableViewCell*)cell;
    NSString *winnerStr = [NSString stringWithFormat:@"%@ : %@",[object valueForKey:@"winner"],[object valueForKey:@"winnerScore"]];
    NSString *looserStr = [NSString stringWithFormat:@"%@ : %@",[object valueForKey:@"looser"],[object valueForKey:@"looserScore"]];
    
    if([[object valueForKey:@"interchangeFlag"] boolValue]){
        customCell.player1Label.text = looserStr;
        customCell.player2Label.text = winnerStr;
    }
    else{
        customCell.player1Label.text = winnerStr;
        customCell.player2Label.text = looserStr;
        
    }
    
}


